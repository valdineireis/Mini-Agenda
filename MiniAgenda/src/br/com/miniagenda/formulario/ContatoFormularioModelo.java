    /**
     * Método responsável por preencher a tabela de contatos
     */
    private void preencherTabela() {
        List<Contato> listaDeContatos;
        try {
            // cria um novo objeto do tipo ContatoDao
            contatoDao = new ContatoDao();
            
            // chama o método 'listar', para buscar todos os contatos
            // em seguida, o resultado da busca é adicionado na variavel 
            // listaDeContatos
            listaDeContatos = contatoDao.listar();

            // trecho de código responsavel por limpar a tabela, para receber
            // novas informações
            DefaultTableModel modelo = (DefaultTableModel) tblContatos.getModel();
            modelo.setRowCount(0);

            // se a variavel 'listaDeContatos' tiver algum valor, será listado
            // todos os contatos encontrados
            if (listaDeContatos.iterator().hasNext()) {
                for (Contato contato : listaDeContatos) {
                    // pega cada contato da 'listaDeContatos' e adiciona em uma 
                    // linha da tabela
                    modelo.addRow(new Object[]{
                                contato.getId(),
                                contato.getNome(),
                                contato.getTelefone()
                            });
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao listar Contatos!\n" + ex.getMessage());
        }
    }


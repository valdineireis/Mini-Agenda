    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        // Efetuar validação dos campos
        String msg = ""; // variavel que armazenará as mensagens de erro
        
        // verifica o telefone
        if (txtTelefone.getText().trim().equals("")) {
            msg += "Preencha o compo telefone.\n";
            txtTelefone.requestFocus();
        }
        // verifica o nome
        if (txtNome.getText().trim().equals("")) {
            msg += "Preencha o compo Nome.";
            txtNome.requestFocus();
        }
        
        // se a variavel 'msg' for diferente de "" (vazio) é porque 
        // ocorreu algum erro, então será exibida uma menssagem e a
        // execução do programa será interrompida
        if ( ! msg.equals("") ) {
            JOptionPane.showMessageDialog(null, msg);
            return;
        }
        
        // cria um objeto do tipo ContatoDao
        contatoDao = new ContatoDao();

        // Cria um novo contato, com os dados informados no formulário
        Contato novoContato = new Contato();
        novoContato.setId(idContatoSelecionado);
        novoContato.setNome(txtNome.getText());
        novoContato.setTelefone(txtTelefone.getText());

        try { // o try serve para capturar eventuais erros

            // se a variavel 'idContatoSelecionado' for igual a 0 (zero)
            // é porque nenhum contato foi selecionado, entao será chamado o 
            // codigo para inserir as informações no bando de dados
            if (idContatoSelecionado == 0L) {

                // código para adicionar novo contato

                contatoDao.inserir(novoContato);

                JOptionPane.showMessageDialog(null, "Contato adicionado com sucesso");

            } 
            // se a variavel 'idContatoSelecionado' for diferente de 0 (zero)
            // é porque foi selecionado algum contato na tabela, entao será 
            // chamado o código para alterar as informações gravadas no bando de dados
            else {

                // código para alterar o contato selecionado

                contatoDao.atualizar(novoContato);

                JOptionPane.showMessageDialog(null, "Contato atualizado com sucesso");

            }

            preencherTabela();
            limparCampos();

        } catch (Exception ex) {
            // caso ocorra algum problema, será exibida uma 
            // mensagem para o usuário, informando do erro
            JOptionPane.showMessageDialog(null, "Erro. Operação não realizada!\n" + ex.getMessage());
        }

    }//GEN-LAST:event_btnSalvarActionPerformed


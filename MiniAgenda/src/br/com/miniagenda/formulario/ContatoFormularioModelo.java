    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

        // pega a linha selecionada
        Integer linha = tblContatos.getSelectedRow();

        // se o usuario nao selecionou nenhuma linha da tabela, o sistema
        // exibe uma mensagem solicitando a selecao
        if (linha < 0) {
            JOptionPane.showMessageDialog(null, "Selecione um contato!");
            return;
        }

        // pega o ID do contato, com base na linha selecionada
        Long codigo = (Long) tblContatos.getValueAt(linha, 0);

        // cria um novo objeto do tipo ContatoDao
        contatoDao = new ContatoDao();
        try {

            // busca um contato pelo ID, e armazena o resultado no objeto 'contato'
            Contato contato = contatoDao.buscarPorId(codigo);

            idContatoSelecionado = contato.getId();
            txtNome.setText(contato.getNome());
            txtTelefone.setText(contato.getTelefone());

            // seleciona o segundo painel
            jTabbedPane1.setSelectedIndex(1);
            txtNome.requestFocus(); // direciona o foco para o campo txtNome

            lblAcao.setText("Alterar Contato");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao localizar Contato!\n" + ex.getMessage());
        }

    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed

        // pega a linha selecionada
        Integer linha = tblContatos.getSelectedRow();

        // se o usuario nao selecionou nenhuma linha da tabela, o sistema
        // exibe uma mensagem solicitando a selecao
        if (linha < 0) {
            JOptionPane.showMessageDialog(null, "Selecione um contato!");
            return;
        }

        // pega o ID do contato, com base na linha selecionada
        Long codigo = (Long) tblContatos.getValueAt(linha, 0);

        // Aqui eh feito uma pergunta ao usuario, se ele deseja realmente
        // excluir o contato selecionado

        Object[] simNao = {"Sim", "Não"};
        int operacao = JOptionPane.showOptionDialog(
                null,
                "Deseja realmente excluir este Contato?",
                "Atenção",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null, simNao, simNao[1]);

        // se clicou em NAO, o sistema interrompe a execucao
        if (operacao == JOptionPane.NO_OPTION) {
            return;
        }

        // cria um novo objeto do tipo ContatoDao
        contatoDao = new ContatoDao();
        try {

            // chama o método excluir, contido dentro da classe ContatoDao
            contatoDao.excluir(codigo);
            preencherTabela(); // após a exclusao, a tabela é atualizada

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao excluir Contato!\n" + ex.getMessage());
        }

    }//GEN-LAST:event_btnExcluirActionPerformed

